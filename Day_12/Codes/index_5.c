#include<stdio.h>
int main(){
        double number=12.45;
        printf("%lf\n",number);
        printf("%.2lf\n",number);

        float number1=10.9f;
        printf("%f\n",number1);
        printf("%.1f\n",number1);

        char character='z';
        printf("%c\n",character);
        printf("%d\n",character);


        return 0;
}
